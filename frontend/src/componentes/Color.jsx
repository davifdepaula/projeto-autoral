export const color = (type) => {
  if (type === "steel") {
    return "#5695a3";
  }
  if (type === "fire") {
    return "#dc872f";
  }

  if (type === "grass") {
    return "#5fbd58";
  }

  if (type === "electric") {
    return "#f2d94e";
  }

  if (type === "water") {
    return "#75d0c1";
  }

  if (type === "ice") {
    return "#75d0c1";
  }

  if (type === "ground") {
    return "#da7c4d";
  }

  if (type === "rock") {
    return "#a38c21";
  }

  if (type === "fairy") {
    return "#ee90e6";
  }

  if (type === "poison") {
    return "#b763cf";
  }

  if (type === "bug") {
    return "#92bc2c";
  }

  if (type === "dragon") {
    return "#0c69c8";
  }

  if (type === "psychic") {
    return "#ff2ca8";
  }

  if (type === "flyng") {
    return "#a1bbec";
  }

  if (type === "fighting") {
    return "#d3425f";
  }

  if (type === "normal") {
    return "#a0a29f";
  }

  if (type === "dark") {
    return "#595761";
  }

  if (type === "ghost") {
    return "#5f6dbc";
  }
};
